package com.itrus.ca.modules.service;

public class ConfigAppBean {
	private Long appId;
	private String appName;
	private String appSupportCommon;
	public Long getAppId() {
		return appId;
	}
	public String getAppName() {
		return appName;
	}
	public String getAppSupportCommon() {
		return appSupportCommon;
	}
	public void setAppId(Long appId) {
		this.appId = appId;
	}
	public void setAppName(String appName) {
		this.appName = appName;
	}
	public void setAppSupportCommon(String appSupportCommon) {
		this.appSupportCommon = appSupportCommon;
	}
	
	
}
