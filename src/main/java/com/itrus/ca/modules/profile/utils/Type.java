package com.itrus.ca.modules.profile.utils;

public class Type {
	private int id;
	private String name;
	private boolean kebl;
	public boolean isKebl() {
		return kebl;
	}
	public void setKebl(boolean kebl) {
		this.kebl = kebl;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
